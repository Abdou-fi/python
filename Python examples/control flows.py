# If statement
print('\n1. -------------------------------------------------') 
x = 10
if x > 5:
    print("x is greater than 5")
else:
    print("x is not greater than 5")


# If-elif-else statement
print('\n2. -------------------------------------------------')
y = 15  
if y > 20:
    print("y is greater than 20")
elif y > 10:
    print("y is greater than 10")
else:
    print("y is not greater than 10")

